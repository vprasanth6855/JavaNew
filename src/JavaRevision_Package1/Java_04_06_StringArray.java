package JavaRevision_Package1;

public class Java_04_06_StringArray {

	public static void main(String[] args) {
		
		String names[] = new String[3];
		names[0] = "Madhav";
		names[1] = "Prasanth";
		names[2] = "Neerav";
		
		for(int i=0; i<names.length; i++){
			System.out.println(names[i]);
		}
	}

}
