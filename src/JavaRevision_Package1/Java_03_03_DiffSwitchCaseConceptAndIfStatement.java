package JavaRevision_Package1;

public class Java_03_03_DiffSwitchCaseConceptAndIfStatement {

	public static void main(String[] args) {
		
		//switch case
		int num =20;
		
	switch (num) {
	case 10:
		System.out.println(10);
		break;
	case 20:
		System.out.println(20);
		break;
	default:
		System.out.println("number not found");
		break;
	}
	
	//if conditional statement
	 int num1 = 30;
	 
	 if(num1==10){
		 System.out.println(10);
	 }
	 else if(num1==20){
		 System.out.println(20);
	 }
	 else{
		 System.out.println("number not found");
	 }
	
	

}

}
